DROP TABLE IF EXISTS MEMBER_DETAIL;

CREATE TABLE MEMBER_DETAIL (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  ZIPCODE VARCHAR(50),
  ADDRESS VARCHAR(50),
  PRIMARY KEY(ID)
);

DROP TABLE IF EXISTS MEMBER;

CREATE TABLE MEMBER (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  USERNAME VARCHAR(50),
  MEMBER_DETAILID BIGINT,
  PRIMARY KEY(ID),
  FOREIGN KEY (MEMBER_DETAILID) REFERENCES MEMBER_DETAIL(ID)
);


