DROP TABLE IF EXISTS STUDENT;
DROP TABLE IF EXISTS PHONE;

CREATE TABLE STUDENT (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  FIRST_NAME VARCHAR(50),
  PRIMARY KEY(ID)
);

CREATE TABLE PHONE (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  COMMENT VARCHAR(50),
  NUMBER VARCHAR(50),
  PRIMARY KEY(ID)
);