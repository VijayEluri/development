DROP TABLE IF EXISTS STUDENT;

CREATE TABLE STUDENT (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(50),
  ADDRESS_ID BIGINT,
  PRIMARY KEY(ID)
);

DROP TABLE IF EXISTS ADDRESS;

CREATE TABLE ADDRESS (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  STREET VARCHAR(50),
  CITY VARCHAR(50),
  ZIPCODE VARCHAR(50),
  PRIMARY KEY(ID)
);