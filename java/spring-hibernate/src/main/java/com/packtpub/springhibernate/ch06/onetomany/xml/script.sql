DROP TABLE IF EXISTS SCHOOL;

CREATE TABLE SCHOOL (
  SCHOOL_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(50),
  PRIMARY KEY(SCHOOL_ID)
);

DROP TABLE IF EXISTS STUDENT;

CREATE TABLE STUDENT (
  STUDENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  FIRST_NAME VARCHAR(50),
  SCHOOL_ID BIGINT,
  PRIMARY KEY(STUDENT_ID)
);